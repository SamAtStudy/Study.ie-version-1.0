            <p class="lead">I was driven to start Study.ie in 2017, after failing to find a peer-reviewed Spanish course in Valencia. After searching through Google and sifting through offerings I was overwhelmed by all of the different website designs and content layouts. I was looking for a recommendation from a peer similar to the way I would find a hotel based on user reviews and that was nowhere to be found. So we started Study.ie.</p>
            <p>Study.ie is a learning resource aggregator that is simplifying the search process and allowing people to record and share their learning experience in order to both track what they did and share it with others. Being a Trinity student, it’s a great opportunity to take part in LaunchBox, Tangent’s Student Accelerator. In addition to the funding, the opportunity to work with like-minded individuals was one of the core reasons why I decided to apply. During the first few weeks, LaunchBox really stress-tested our idea. Each week on the programme a different team runs a peer session. You have to present on a challenge you’re facing, or a product feature you’d like tested – anything that means you’re open to feedback and criticism, and that pulls you out of your startup bubble.</p>
            <p>So how did I find out where to start learning to code. Back in 2014 I did what most of Generation Z does to find an answer, I googled it and I what I got back was an avalanche of information well into the hundreds of thousands of search results. That’s when I realised the problem wasn’t with what I was searching. I asked and google answered the problem was how I was searching. So that was my next question for the internet “how do I search on google” and what I learned from that simple search has affected the way I learn throughout my whole life. Secondary school through college onwards.</p>
            <h2>“Study.ie is a learning resource aggregator that is simplifying the search process and allowing people to record and share their learning experience”</h2>
            <p>It was both a blessing and a curse to have our session early; it pushed us to get our website prototype done faster and to have the cohort use it and give us some feedback which really opened our eyes to what people wanted. Even though the session shook me and the Study.ie team to the core, and showed me that there is still a lot to learn, the insights from the other LaunchBox startups in conjunction with the guest founders has really given me confidence. Sometimes done is better than perfect.</p>

