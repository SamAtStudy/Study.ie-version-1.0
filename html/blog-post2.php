            <p class="lead">The idea of learning to code is daunting task. As the need for countries to churn out as many computer scientists and engineers as possible grows so does the amount of free information available for the curious “beginner”. </p>
            <p>With the use of search engines skyrocketing in the last decade and a half so has the amount of information accessible. With so many tutorials and online courses research reports readily available where are you meant to start. To give some context 500 hours of content is uploaded to YouTube every minute. That’s an unfathomable number. </p>
            <p>So how did I find out where to start learning to code. Back in 2014 I did what most of Generation Z does to find an answer, I googled it and I what I got back was an avalanche of information well into the hundreds of thousands of search results. That’s when I realised the problem wasn’t with what I was searching. I asked and google answered the problem was how I was searching. So that was my next question for the internet “how do I search on google” and what I learned from that simple search has affected the way I learn throughout my whole life. Secondary school through college onwards.</p>
            <h5>The Steps:</h5>
            <p>1.	Double quotes and when to use them when you google something with “quotes surrounding it” your telling google show me results containing only this word.
                How to code “java” vs how to code java gives you 200,000 search results less and therefore more accuracy for what you’re actually searching.</p>
            <p>2.	On the flip side writing a question how to code -java removes all results containing java.</p>
            <p>3.	Asterisks, perhaps a friend recommended a website that your struggling to remember the full name of. * can be used to fill in missing words.</p>
            <p>4.	Reverse image searching this can sound like a mouthful but all it does is take whatever image you upload to search, and scan google for the same image
                This function is less than perfect but can be used in a pinch.</p>
            <p>Every time I set out to learn something new, using these four tools I’ve been able to focus in on specifics I found confusing or interesting on my learning journey and cut out aspects I already knew.
                now so can you.</p>
