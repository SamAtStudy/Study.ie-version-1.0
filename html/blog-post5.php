            <p class="lead">To start off, let me just state that learning is extremely boring. There is nothing that is fun about the process of learning, you muster up the strength and willpower to learn something through a big burst of excitement and then you start the journey. You put in an hour or so and think to yourself, "I'm loving this, this is really great." What you fail to notice, is that you have only crossed the starting line and you have a whole marathon yet to run.  </p>
            <h3>Why we are so bad at learning? </h3>
            <p>One of our worst traits as humans is that we lack consistency in a lot of things that we tend to do. The second we tend to get bored of something we just throw it to the side and say "maybe tomorrow". Tim Ferris has this quote, "Tomorrow is never", just like that, what we were so excited to learn to a 'Master' level, we drop off the plate into the bin like it never mattered anyway. That 'JavaScript Tech Guru' or 'Most Influential Investor To Ever Walk The Face Of The Earth' that you had so much ambition to become, just gets wiped off the whiteboard of dreams.  </p>            <p>So how did I find out where to start learning to code. Back in 2014 I did what most of Generation Z does to find an answer, I googled it and I what I got back was an avalanche of information well into the hundreds of thousands of search results. That’s when I realised the problem wasn’t with what I was searching. I asked and google answered the problem was how I was searching. So that was my next question for the internet “how do I search on google” and what I learned from that simple search has affected the way I learn throughout my whole life. Secondary school through college onwards.</p>
            <h3>So what is the secret sauce to overcoming inconsistency in learning? </h3>
            <p>Think of a sport or a hobby that you would say you're very good at, now ask yourself what got you to the level you're at right now? In 99.9% of cases it will be because of consistent training, failing, more training, and even more failing. When we start learning a sport for example we don't just do it for 6 hours straight, 5 days in a row and expect to be a 'pro'. You do a few sessions a week and then over the course of a few years you get better and better without even noticing. You were enjoying yourself so much along the way that you we're even thinking about how consistent you were with it. Shouldn't learning something treated the same? It is necessary to plan out how you will go about learning something and how much time you can dedicate before you take the plunge into something you know absolutely nothing about</p>
            <h3>Conclusion  </h3>
            <p>Leaning is boring in the sense that it is very hard to maintain excitement in something new that you're learning. It is much simpler to nourish some passion and love by simply consistently applying yourself. Action leads to motivation, which then leads to inspiration, and then inspiration leads you back to action, over and over. You don't have to take on catching all the fish in the sea at once, you do it one by one, time after time. And you will never catch all the fish in the sea, but the few that you do catch you had a fun and relaxed time doing it.  </p>
