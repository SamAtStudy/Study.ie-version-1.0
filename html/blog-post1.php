                <p>and I feel that in the current day and age, of constant technological disruption, we have more resources and capacity than ever to be able to greatly impact the world but we also need to constantly think and keep in mind the unintended repercussions of our disruptive ventures.</p>
                <p>So while working and considering which forms of learning, and which experiences, are at the greatest risk of being fundamentally changed as a result of the current digitisation of the education environment. I thought of how online lectures are both an opportunity to learn and get valuable insights form top class lecturers or speakers, while at the same time losing the impact and accessibility to certain types of learners.</p>
