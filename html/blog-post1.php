                <p>I am not a student within the field of technology and automation but I have often heard of people discussing how many jobs in the field of manufacturing are being lost due to automation of the process. This being my percieved core worry among many, I feel that there is going to be a great universal shock when they find out that, it is in fact, teachers who are going to be the ones who are slowly being replaced by online learning.</p>
                <p>More and more do we hear of the individuals who are self taught as a result of amazing online resources such as Youtube and online learning platforms that we forget that there are many different types of learning and that a method that suits one individual may not be the best for someone else.</p>
