            <p class="lead"> When we think of learning, we should look at it as something that is of benefit to others along with the benefit it brings to us personally. There is little or no value in learning something only for yourself as  its commonly stated that one of the great joys in learning is the ability to teach. Think back to a time when someone asked you to explain something that you knew better that the other person, and  try remember that feeling of content and satisfaction you had in being of value to that person at that particular time. True knowledge is the ability to share it with other people. Robert Noyce, co-founder of Fairchild Semiconductor and Intel has this famous quote which goes, "Knowledge is power. Knowledge shared is power multiplied."</p>
            <br>
            <h3>Importance of sharing learning advice</h3>
            <p>I can't stress how critical and frequent sharing learning resources and advice is in our society. Most of the things we learn are either because someone nudged us into it, or signalled that it's a good thing to learn. It's much easier to trust another person who's already done it before, than starting your search and adventure on your own. Much more people end up making poor decisions than they even realise. You should be learning from other people's mistakes when it comes to learning resources. The person you're usually asking for learning advice has more often than not made his own mistakes, by asking this person you are naturally avoiding these errors and mistakes as they most likely won't give you the bad resources, but rather something better that they have found (P.S. they might not give you the best resources if they don't like you, so try find someone you can trust, you can be sure likes you or who is an expert in the area).</p>
            <h3>How to help others by sharing what you know</h3>
            <p>
                Everyone is a lifelong learner, whether they want to admit it or not. So don't be shy to ask what other people are learning in their lives and if you have anything that could be of value to them don't be afraid to mention it to them. Another great place to start is discussion boards and then answer some questions that you may have answers to. Other things that I would recommend others to do, would be:
            </p>
            <p>1. Start a personal blog</p>
            <p>2. Share resources and tips on personal social medias</p>
            <p>3. Start a podcast (all you need is a phone mic)</p>
            <p>4. Talk about learning a little bit more</p>
            <br>
            <h3>Conclusion</h3>
            <p>Although this may seem very obnoxious or whatnot, as long as you want to help other people, even if you only help one other person or even inspire them to do something it's already worth it. People think that in order to have an impact you need to reach a large amount of people, or have a large follower base, in reality all you need is the desire to help other people. If you can find the courage and willingness to do this you are already making a greater positive impact than you may think.</p>
